package com.uestc.newmodel;

public class ClassInfoStr {
	private String gradeName;
	private String specialityName;
	private String classId;
	private String classTeacherId;
	private String classTeacherName;
	private String schoolYearType;
	private String classSize;
	public String getGradeName() {
		return gradeName;
	}
	public void setGradeName(String gradeName) {
		this.gradeName = gradeName;
	}
	public String getSpecialityName() {
		return specialityName;
	}
	public void setSpecialityName(String specialityName) {
		this.specialityName = specialityName;
	}
	public String getClassId() {
		return classId;
	}
	public void setClassId(String classId) {
		this.classId = classId;
	}
	public String getClassTeacherId() {
		return classTeacherId;
	}
	public void setClassTeacherId(String classTeacherId) {
		this.classTeacherId = classTeacherId;
	}
	public String getClassTeacherName() {
		return classTeacherName;
	}
	public void setClassTeacherName(String classTeacherName) {
		this.classTeacherName = classTeacherName;
	}
	public String getSchoolYearType() {
		return schoolYearType;
	}
	public void setSchoolYearType(String schoolYearType) {
		this.schoolYearType = schoolYearType;
	}
	public String getClassSize() {
		return classSize;
	}
	public void setClassSize(String classSize) {
		this.classSize = classSize;
	}
	

}
