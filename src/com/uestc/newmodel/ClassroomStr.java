package com.uestc.newmodel;

public class ClassroomStr {
	private String classroomId;//教师编号

    

    private String classType;//教室类型

    private Integer classroomSize;//教室容纳人数

	public String getClassroomId() {
		return classroomId;
	}

	public void setClassroomId(String classroomId) {
		this.classroomId = classroomId;
	}

	public String getClassType() {
		return classType;
	}

	public void setClassType(String classType) {
		this.classType = classType;
	}

	public Integer getClassroomSize() {
		return classroomSize;
	}

	public void setClassroomSize(Integer classroomSize) {
		this.classroomSize = classroomSize;
	}

    
}
